package in.presso.pojo;

public class PojoArea {
	public int AreaId;

	public int getAreaId() {
		return AreaId;
	}

	public void setAreaId(int areadId) {
		AreaId = areadId;
	}

	public String getAreaName() {
		return AreaName;
	}

	public void setAreaName(String areadName) {
		AreaName = areadName;
	}

	public int getCity() {
		return City;
	}

	public void setCity(int city) {
		City = city;
	}

	public String AreaName;
	public int City;
}
